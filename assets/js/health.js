const audioTriggerFirstPara = document.getElementById(
  "audioTrigger-first-para"
);
const firstAudio = document.getElementById("firstAudio");

audioTriggerFirstPara.addEventListener("click", function () {
  if (firstAudio.paused) {
    firstAudio.play();
  } else {
    firstAudio.pause();
  }
});

